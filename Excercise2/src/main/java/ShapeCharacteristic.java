/**
 *  this class is the biggest class of all geometry, it has method getPerimeter and getArea which all other object have
 */
public interface ShapeCharacteristic {
    /**
     *
     * @return the perimeter of the object
     */
    public float getPerimeter();

    /**
     *
     * @return the Area number of all object
     */
    public float getArea();
}
